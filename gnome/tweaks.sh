#!/bin/bash

gsettings set org.gnome.desktop.wm.preferences titlebar-uses-system-font true
gsettings set org.gnome.desktop.wm.preferences titlebar-font 'SauceCodePro NF Bold 11'

gsettings set org.gnome.desktop.interface font-name 'SauceCodePro NF 11'
gsettings set org.gnome.desktop.interface monospace-font-name 'SauceCodePro NF 13'
gsettings set org.gnome.desktop.interface document-font-name 'SauceCodePro NF Medium 11'

gsettings set org.gnome.desktop.interface cursor-theme 'Bibata-Modern-DodgerBlue'
gsettings set org.gnome.desktop.interface gtk-theme 'Flat-Remix-GTK-Violet-Darkest'
gsettings set org.gnome.desktop.interface icon-theme 'Yaru-magenta-dark'
