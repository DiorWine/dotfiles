#!/bin/bash

gsettings set org.gnome.desktop.wm.preferences titlebar-uses-system-font false
gsettings set org.gnome.desktop.wm.preferences titlebar-font 'SauceCodePro NF Medium 16'
gsettings set org.gnome.desktop.interface font-name 'SauceCodePro NF 13'
gsettings set org.gnome.desktop.interface monospace-font-name 'SauceCodePro NF 13'
gsettings set org.gnome.desktop.interface document-font-name 'SauceCodePro Nerd Font Medium 11'

gsettings set org.gnome.desktop.interface gtk-theme 'Flat-Remix-GTK-Green-Darkest-Solid-NoBorder'
gsettings set org.gnome.desktop.interface icon-theme 'Flat-Remix-Green-Dark'
